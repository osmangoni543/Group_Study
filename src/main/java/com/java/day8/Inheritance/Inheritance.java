package com.java.day8.Inheritance;

public class Inheritance {
	

	/*
	 * In Java, through inheritance, a subclass (or derived class) can inherit the
	 * following members from its superclass (or base class):
	 * 
	 * Fields (Variables): Subclasses inherit all non-private fields from their
	 * superclass. This includes instance variables and class (static) variables.
	 * 
	 * Methods: Subclasses inherit all non-private methods from their superclass.
	 * This includes both instance methods and class (static) methods.
	 * 
	 * Constructors: Subclasses inherit all constructors from their superclass.
	 * However, constructors are not directly inherited because they are not
	 * members, but the subclass must invoke a superclass constructor using super()
	 * in its own constructor.
	 * 
	 * Nested Classes: Subclasses inherit nested classes (static inner classes) from
	 * their superclass.
	 * 
	 * Initializer Blocks: Subclasses inherit static and instance initializer blocks
	 * from their superclass.
	 * 
	 * Annotations: Subclasses inherit annotations applied to fields, methods, and
	 * classes from their superclass.
	 */

}
